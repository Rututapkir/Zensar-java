public class twoDarray {
    public static void main(String args[]){
        int matrix[][]={{100,200,300,400},{200,800,500,600}};
        System.out.println(matrix[1][0]);
        System.out.println("Size of Row is "+matrix.length);
        System.out.println("Size of Column is "+matrix[0].length);
        for(int i=0;i<matrix.length;i++){
            for(int j=0;j<matrix[0].length;i++){
                System.out.print(matrix[matrix.length][matrix[0].length]);
            }
        }
        
    }
    
}
